    public void setEditando(boolean editando, VariedadFruto variedad){
        if (variedad != null) {
            this.variedad = variedad;
            txtVariedad.setText(variedad.getNombre());
            txtCaracteristicas.setText(variedad.getCaracteristicas());
        }
        this.editando = editando;
    }
    
