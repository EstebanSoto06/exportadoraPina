    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String variedad = txtVariedad.getText();
        String caracteristicas = txtCaracteristicas.getText();
        
        if (variedad.isEmpty() || caracteristicas.isEmpty()){
            JOptionPane.showMessageDialog(this, "Verifique ningún dato esté en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            VariedadesDB gestor = new VariedadesDB();
            if (editando){
                gestor.editarVariedad(this.variedad.getId(), variedad, caracteristicas);
            } else {
                gestor.agregarVariedad(variedad, caracteristicas);
            }
        }
        
        crud.cargarDatosTabla();
        crud.setEnabled(true);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

