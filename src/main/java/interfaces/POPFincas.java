    public void setEditando(boolean editando, Finca finca){
        if (finca != null) {
            this.finca = finca;
            txtFinca.setText(finca.getNombre());
            txtUbicacion.setText(finca.getUbicacion());
            txtProveedor.setText(String.valueOf(finca.getId_proveedor()));
        }
        this.editando = editando;
    }
    
