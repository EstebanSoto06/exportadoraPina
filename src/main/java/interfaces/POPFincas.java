    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtFinca.getText();
        String ubicacion = txtUbicacion.getText();
        String id_proveedor = txtProveedor.getText();

        if (nombre.isEmpty() || ubicacion.isEmpty() || id_proveedor.isEmpty()){
            JOptionPane.showMessageDialog(this, "Verifique ningún dato esté en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            FincasDB gestor = new FincasDB();
            if (editando){
                gestor.editarFinca(this.finca.getId(), nombre, ubicacion, Integer.parseInt(id_proveedor));
            } else {
                gestor.agregarFinca(nombre, ubicacion, Integer.parseInt(id_proveedor));
            }
            
            crud.cargarDatosTabla();
            crud.setEnabled(true);
            this.dispose();
        }  
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtProveedorKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtProveedorKeyTyped
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;
        
        if (!numeros)
        {
            evt.consume();
        }

        if (txtProveedor.getText().trim().length() == 10) {
            evt.consume();
        }   
    }//GEN-LAST:event_txtProveedorKeyTyped

    private void btnBuscarProveedorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarProveedorActionPerformed
        FincasDB gestor = new FincasDB();
        String nombre;
        nombre = gestor.obtenerNombreProveedor(Integer.parseInt(txtProveedor.getText()));
        lbNombreProveedor.setText(nombre);
    }//GEN-LAST:event_btnBuscarProveedorActionPerformed

    
