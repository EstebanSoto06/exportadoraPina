    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String nombre = txtProveedor.getText();
        String encargado = txtEncargado.getText();
        String telefono = txtTelefono.getText();
        String correo = txtCorreo.getText();
        
        if (nombre.isEmpty() || encargado.isEmpty() || telefono.isEmpty() || correo.isEmpty()){
            JOptionPane.showMessageDialog(this, "Verifique ningún dato esté en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            ProveedoresDB gestor = new ProveedoresDB();
            if (editando){
                gestor.editarProveedor(this.proveedor.getId(), nombre, encargado, telefono, correo);
            } else {
                gestor.agregarProveedor(nombre, encargado, telefono, correo);
            }
        }
        
        crud.cargarDatosTabla();
        crud.setEnabled(true);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtTelefonoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtTelefonoKeyTyped
        // IMPIDE DIGITAR OTRA COSA QUE NO SEAN NUMEROS
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;
        
        if (!numeros)
        {
            evt.consume();
        }

        if (txtTelefono.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_txtTelefonoKeyTyped

