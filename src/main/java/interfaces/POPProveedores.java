    public void setEditando(boolean editando, Proveedor proveedor){
        if (proveedor != null) {
            this.proveedor = proveedor;
            txtProveedor.setText(proveedor.getNombre());
            txtEncargado.setText(proveedor.getEncargado());
            txtTelefono.setText(String.valueOf(proveedor.getTelefono()));
            txtCorreo.setText(proveedor.getCorreo());
        }
        this.editando = editando;
    }
    
