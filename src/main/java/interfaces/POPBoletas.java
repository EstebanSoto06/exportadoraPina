    public void setComboBox(List<Map<Integer, String>> proveedores, List<Map<Integer, String>> fincas, List<Map<Integer, String>> tipos){
        this.proveedores = proveedores;
        this.fincas = fincas;
        this.tipos = tipos;
        
        cbProveedores.removeAllItems();
        cbFincas.removeAllItems();
        cbTipos.removeAllItems();
        
        for(int i = 0; i < proveedores.size(); i++){
            Map<Integer, String> proveedor = proveedores.get(i);
            for (Map.Entry<Integer, String> entry : proveedor.entrySet()) {
                cbProveedores.addItem(entry.getValue());
            }
        }
        
        for(int i = 0; i < fincas.size(); i++){
            Map<Integer, String> finca = fincas.get(i);
            for (Map.Entry<Integer, String> entry : finca.entrySet()) {
                cbFincas.addItem(entry.getValue());
            }
        }
        
        for(int i = 0; i < tipos.size(); i++){
            Map<Integer, String> tipo = tipos.get(i);
            for (Map.Entry<Integer, String> entry : tipo.entrySet()) {
                cbTipos.addItem(entry.getValue());
            }
        }
    }
