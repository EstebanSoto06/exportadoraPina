    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        String lote = txtLote.getText();
        String bloque = txtBloque.getText();
        String numeroBinA = txtNumeroBinA.getText();
        String numeroBinB = txtNumeroBinB.getText();
        int cantidadBinA = Integer.parseInt(txtFrutasBinA.getText());
        int cantidadBinB = Integer.parseInt(txtFrutasBinB.getText());
        String placaCamion = txtPlacaCamion.getText();
        
        int selectedProveedor = cbProveedores.getSelectedIndex();
        int selectedFinca = cbFincas.getSelectedIndex();
        int selectedTipo = cbTipos.getSelectedIndex();
        
        if (lote.isEmpty() || bloque.isEmpty() || numeroBinA.isEmpty() || numeroBinB.isEmpty() || Integer.toString(cantidadBinA).isEmpty() || Integer.toString(cantidadBinB).isEmpty() || placaCamion.isEmpty()){
            JOptionPane.showMessageDialog(this, "Verifique ningún dato esté en blanco", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            BoletasDB gestor = new BoletasDB();
            
            Map<Integer, String> proveedor = this.proveedores.get(selectedProveedor);
            Map<Integer, String> finca = this.fincas.get(selectedFinca);
            Map<Integer, String> tipo = this.tipos.get(selectedTipo);
            
            int id_proveedor = proveedor.keySet().iterator().next();
            int id_finca = finca.keySet().iterator().next();
            int id_tipoFruto = tipo.keySet().iterator().next();
            
            System.out.println(selectedProveedor);
            System.out.println(selectedFinca);
            System.out.println(selectedTipo);
            
            System.out.println(id_proveedor);
            System.out.println(id_finca);
            System.out.println(id_tipoFruto);
            
            String fecha;
            SimpleDateFormat formatter = new SimpleDateFormat("HH:mm dd/MM/yyyy");
            fecha = formatter.format(new Date());
            
            if (editando){
                BoletaCosecha boletaEdit = new BoletaCosecha(this.boleta.getId(), id_proveedor, id_finca, lote, bloque, numeroBinA, cantidadBinA, numeroBinB, cantidadBinB, placaCamion, id_tipoFruto);
                gestor.editarBoleta(boletaEdit);
            } else {
                BoletaCosecha boleta = new BoletaCosecha(0, id_proveedor, id_finca, lote, bloque, numeroBinA, cantidadBinA, numeroBinB, cantidadBinB, placaCamion, id_tipoFruto);
                gestor.agregarBoleta(boleta);
                gestor.editarConsecutivoBoleta();
            }
        }
        
        crud.cargarDatosTabla();
        crud.setEnabled(true);
        this.dispose();
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void txtLoteKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtLoteKeyTyped
        // IMPIDE DIGITAR OTRA COSA QUE NO SEAN NUMEROS
        int key = evt.getKeyChar();

        boolean numeros = key >= 48 && key <= 57;
        
        if (!numeros)
        {
            evt.consume();
        }

        if (txtLote.getText().trim().length() == 10) {
            evt.consume();
        }
    }//GEN-LAST:event_txtLoteKeyTyped

